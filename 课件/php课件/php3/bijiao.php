<?php

//  比较运算符  
//      <    >    <=   >=    <> !      !=   !==   ==   === 



var_dump(5>4);   // true

var_dump(5<4);   //  false

var_dump(5==4);  //  false

var_dump(5===5);  //  true

var_dump(5!==4);  //  true


$a = '1234';

$b= 1234;

var_dump($a==$b);  //  值比较

var_dump($a===$b); //   值比较    还会对 数据类型进行比较



?>











